class Calculator:

    def add(self, a: object, b: object) -> object:
        return a + b

    def sub(self, a: object, b: object) -> object:
        return a - b

    def mul(self, a: object, b: object) -> object:
        return a * b

    def div(self, a: object, b: object) -> object:
        return a / b

